N = int(input())
l = len(str(N))
ans = 0

if l >= 4 and l <= 6:
    ans = N - 999
elif l >= 7 and l <= 9:
    ans = (N - 999999)*2 + (999999 - 999)
elif l >= 10 and l <= 12:
    ans = (N - 999999999)*3 + (999999999 - 999999)*2 + (999999 - 999)
elif l >= 13 and l <= 15:
    ans = (N-999999999999)*4 + (999999999999 - 999999999) * \
        3 + (999999999 - 999999)*2 + (999999 - 999)
elif l >= 16:
    ans = (N-999999999999999)*5 + (999999999999999-999999999999)*4 + \
        (999999999999 - 999999999)*3 + (999999999 - 999999)*2 + (999999 - 999)

print(ans)
